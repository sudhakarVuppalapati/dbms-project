package util;

/**
 * Identifiers for the different relational operators
 * 
 */

public enum RelationalOperatorType {

	/**
	 * An input operator
	 */
	INPUT,
	/**
	 * A join operator
	 */
	JOIN,
	/**
	 * A selection operator
	 */
	SELECTION,
	/**
	 * A projection operator
	 */
	PROJECTION,
	/**
	 * A cross-product operator
	 */
	CROSS_PRODUCT

}
